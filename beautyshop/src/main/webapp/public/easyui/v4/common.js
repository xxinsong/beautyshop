// 绑定事件名称
function addEventByName(name, parentJq, event_name, call_back) {
	jQ("[name='" + name + "']", parentJq || document).bind(event_name,
			call_back);
	return false;
}
// id绑定事件
function addEventById(name, parentJq, event_name, call_back) {
	jQ("[id='" + name + "']", parentJq || document).bind(event_name, call_back);
	return false;
}

function addEventByExpr(name, parentJq, event_name, call_back) {
	jQ(name, parentJq || document).bind(event_name, call_back);
	return false;
}
// 添加class
function addClassByName(express, currJq, className) {
	jQ(express).removeClass(className);
	currJq.addClass(className);
}