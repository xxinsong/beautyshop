// 返回当前页面高度
function pageHeight() {

	if (jQ.browser.msie) {
		return document.compatMode == "CSS1Compat"
				? document.documentElement.clientHeight
				: document.body.clientHeight;
	} else {
		return self.innerHeight;
	}
};

